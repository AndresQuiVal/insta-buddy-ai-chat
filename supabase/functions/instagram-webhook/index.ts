
import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2.38.4'

interface WebhookEntry {
  id: string;
  time: number;
  messaging?: MessagingEvent[];
  changes?: ChangeEvent[];
}

interface MessagingEvent {
  sender: { id: string };
  recipient: { id: string };
  timestamp: number;
  message?: {
    mid: string;
    text: string;
    attachments?: any[];
  };
}

interface ChangeEvent {
  field: string;
  value: {
    from: { id: string };
    item: string;
    created_time: number;
    verb: string;
    messaging?: MessagingEvent[];
  };
}

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    // Verificaci√≥n del webhook
    if (req.method === 'GET') {
      const url = new URL(req.url)
      const mode = url.searchParams.get('hub.mode')
      const token = url.searchParams.get('hub.verify_token')
      const challenge = url.searchParams.get('hub.challenge')

      console.log('üîç Webhook verification:', { mode, token, challenge })

      const verifyToken = Deno.env.get('INSTAGRAM_VERIFY_TOKEN')
      
      if (mode === 'subscribe' && token === verifyToken) {
        console.log('‚úÖ Webhook verified successfully')
        return new Response(challenge, {
          status: 200,
          headers: { 'Content-Type': 'text/plain', ...corsHeaders }
        })
      } else {
        console.log('‚ùå Webhook verification failed')
        return new Response('Forbidden', { 
          status: 403,
          headers: corsHeaders
        })
      }
    }

    // Procesar webhooks POST
    if (req.method === 'POST') {
      const body = await req.json()
      console.log('üì® Instagram webhook received:', JSON.stringify(body, null, 2))

      // Inicializar cliente Supabase
      const supabaseUrl = Deno.env.get('SUPABASE_URL')!
      const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
      const supabase = createClient(supabaseUrl, supabaseServiceKey)

      if (body.object === 'instagram') {
        for (const entry of body.entry as WebhookEntry[]) {
          console.log(`üîÑ Processing entry ${entry.id}`)

          // Procesar mensajes directos
          if (entry.messaging) {
            for (const event of entry.messaging) {
              await processMessagingEvent(supabase, event)
            }
          }

          // Procesar cambios en la p√°gina
          if (entry.changes) {
            for (const change of entry.changes) {
              if (change.field === 'messages' && change.value.messaging) {
                for (const event of change.value.messaging) {
                  await processMessagingEvent(supabase, event)
                }
              }
            }
          }
        }
      }

      return new Response(JSON.stringify({ status: 'success' }), {
        status: 200,
        headers: { 'Content-Type': 'application/json', ...corsHeaders }
      })
    }

    return new Response('Method not allowed', { 
      status: 405,
      headers: corsHeaders
    })
  } catch (error) {
    console.error('‚ùå Webhook error:', error)
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { 'Content-Type': 'application/json', ...corsHeaders }
    })
  }
})

async function processMessagingEvent(supabase: any, event: MessagingEvent) {
  try {
    console.log('üìù Processing messaging event:', event)

    if (!event.message || !event.message.text) {
      console.log('‚è≠Ô∏è Skipping event without text message')
      return
    }

    const messageData = {
      instagram_message_id: event.message.mid,
      sender_id: event.sender.id,
      recipient_id: event.recipient.id,
      message_text: event.message.text,
      message_type: 'received',
      timestamp: new Date(event.timestamp).toISOString(),
      is_read: false,
      raw_data: {
        webhook_data: event,
        received_at: new Date().toISOString(),
        source: 'instagram_webhook'
      }
    }

    console.log('üíæ Saving message to database:', messageData)

    // Verificar si el mensaje ya existe
    const { data: existingMessage } = await supabase
      .from('instagram_messages')
      .select('id')
      .eq('instagram_message_id', event.message.mid)
      .single()

    if (existingMessage) {
      console.log('‚è≠Ô∏è Message already exists, skipping')
      return
    }

    // Guardar el mensaje
    const { data, error } = await supabase
      .from('instagram_messages')
      .insert(messageData)

    if (error) {
      console.error('‚ùå Error saving message:', error)
      throw error
    }

    console.log('‚úÖ Message saved successfully:', data)

    // üî• AN√ÅLISIS Y RESPUESTA ESTRAT√âGICA MEJORADA
    await handleStrategicAnalysisAndResponse(supabase, event.sender.id, event.message.text)

  } catch (error) {
    console.error('‚ùå Error processing messaging event:', error)
    throw error
  }
}

async function handleStrategicAnalysisAndResponse(supabase: any, senderId: string, currentMessage: string) {
  try {
    console.log('üéØ INICIANDO AN√ÅLISIS ESTRAT√âGICO MEJORADO')
    console.log(`üë§ Sender ID: ${senderId}`)
    console.log(`üí¨ Mensaje actual: "${currentMessage}"`)

    // 1. OBTENER CARACTER√çSTICAS IDEALES DESDE LA BASE DE DATOS
    console.log('üîç Obteniendo caracter√≠sticas ideales...')
    const { data: idealTraitsData, error: traitsError } = await supabase
      .from('ideal_client_traits')
      .select('*')
      .eq('enabled', true)
      .order('position')

    if (traitsError) {
      console.error('‚ùå Error obteniendo caracter√≠sticas:', traitsError)
      await sendGenericResponse(supabase, senderId)
      return
    }

    if (!idealTraitsData || idealTraitsData.length === 0) {
      console.log('‚ö†Ô∏è No hay caracter√≠sticas ideales configuradas')
      await sendGenericResponse(supabase, senderId)
      return
    }

    console.log('‚úÖ Caracter√≠sticas ideales cargadas:', idealTraitsData.map(t => t.trait))

    // 2. OBTENER TODO EL HISTORIAL DE CONVERSACI√ìN COMPLETO
    console.log('üìö Obteniendo historial COMPLETO de conversaci√≥n...')
    const { data: allMessages, error: messagesError } = await supabase
      .from('instagram_messages')
      .select('*')
      .or(`sender_id.eq.${senderId},recipient_id.eq.${senderId}`)
      .order('created_at', { ascending: true })

    if (messagesError) {
      console.error('‚ùå Error obteniendo mensajes:', messagesError)
      await sendGenericResponse(supabase, senderId)
      return
    }

    // 3. CONSTRUIR HISTORIAL COMPLETO DE CONVERSACI√ìN
    const conversationHistory = allMessages || []
    const fullConversationText = conversationHistory
      .map(msg => `${msg.message_type === 'received' ? 'Usuario' : 'IA'}: ${msg.message_text}`)
      .join('\n')

    console.log(`üìù Historial completo (${conversationHistory.length} mensajes):`)
    console.log(fullConversationText.substring(0, 500) + '...')

    // 4. AN√ÅLISIS ESTRAT√âGICO MEJORADO
    console.log('üß† Realizando an√°lisis estrat√©gico completo...')
    const analysis = await analyzeConversationStrategically(fullConversationText, idealTraitsData)
    console.log('üìä Resultado del an√°lisis:', analysis)

    // 5. GUARDAR AN√ÅLISIS EN LA BASE DE DATOS
    if (analysis.matchPoints > 0) {
      console.log('üíæ Guardando an√°lisis...')
      await saveAnalysisToDatabase(supabase, senderId, analysis, conversationHistory.length)
    }

    // 6. GENERAR RESPUESTA ESTRAT√âGICA INTELIGENTE
    console.log('ü§ñ Generando respuesta estrat√©gica basada en contexto...')
    const strategicResponse = await generateContextualStrategicResponse(
      supabase,
      senderId,
      currentMessage,
      fullConversationText,
      analysis,
      idealTraitsData
    )

    console.log('üì§ Respuesta estrat√©gica generada:', strategicResponse)

    // 7. ENVIAR RESPUESTA AUTOM√ÅTICA
    await sendResponseWithDelay(supabase, senderId, strategicResponse)

  } catch (error) {
    console.error('‚ùå Error en an√°lisis estrat√©gico:', error)
    await sendGenericResponse(supabase, senderId)
  }
}

