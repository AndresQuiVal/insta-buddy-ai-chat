async function generateContextualStrategicResponse(
  supabase: any,
  senderId: string,
  currentMessage: string,
  fullConversationText: string,
  analysis: any,
  idealTraits: any[]
): Promise<string> {
  try {
    const openaiKey = Deno.env.get('OPENAI_API_KEY')
    
    if (!openaiKey) {
      console.log('‚ö†Ô∏è No hay API key de OpenAI, usando respuesta estrat√©gica predefinida')
      return getContextualFallbackResponse(currentMessage, analysis, idealTraits)
    }

    // Crear prompt ULTRA estrat√©gico con contexto completo
    const strategicPrompt = createAdvancedContextualPrompt(
      currentMessage,
      fullConversationText,
      analysis,
      idealTraits
    )

    console.log('üß† Enviando prompt contextual a OpenAI...')

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openaiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o',
        messages: [
          {
            role: 'system',
            content: strategicPrompt
          },
          {
            role: 'user',
            content: currentMessage
          }
        ],
        max_tokens: 200,
        temperature: 0.8
      })
    })

    if (!response.ok) {
      console.error('‚ùå Error en OpenAI API:', await response.text())
      return getContextualFallbackResponse(currentMessage, analysis, idealTraits)
    }

    const data = await response.json()
    const aiResponse = data.choices[0].message.content

    console.log('‚úÖ Respuesta contextual de OpenAI:', aiResponse)
    return aiResponse

  } catch (error) {
    console.error('‚ùå Error generando respuesta contextual:', error)
    return getContextualFallbackResponse(currentMessage, analysis, idealTraits)
  }
}

